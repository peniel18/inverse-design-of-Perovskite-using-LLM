  write_setting "RCLONE_REMOTE" "$RCLONE_REMOTE_VALUE"
  write_setting "NOTIFIER_PASSWORD_FILE" "$NOTIFIER_PASSWORD_FILE_VALUE"
  chmod 600 "$ENV_FILE"

  cat <<EOF

Saved global configuration to $ENV_FILE
Per-project configs live under $PROJECT_ENV_DIR/<project>.env
EOF
  exit 0
fi

