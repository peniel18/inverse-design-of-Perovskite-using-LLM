#!/usr/bin/env bash
set -euo pipefail
IFS=$'\n\t'

SCRIPT_DIR="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" && pwd -P)"
# shellcheck source=/dev/null
. "$SCRIPT_DIR/lib/config.sh"
load_project_env "$SCRIPT_DIR"

CONFIG_MODE="project"
TARGET_PROJECT="${REMOTE_PILOT_PROJECT:-default}"
CONFIG_DEPTH="${REMOTE_PILOT_CONFIG_DEPTH:-advanced}"

validate_config_depth() {
  case "$1" in
    basic|advanced|advanced-all) return 0 ;;
    *) return 1 ;;
  esac
}

depth_is_at_least() {
  local requested="$1"
  case "$CONFIG_DEPTH" in
    advanced-all) return 0 ;;
    advanced) [[ "$requested" != "advanced-all" ]] ;;
    basic) [[ "$requested" == "basic" ]] ;;
    *) return 1 ;;
  esac
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --project)
      TARGET_PROJECT="${2:-}"
      shift 2
      ;;
    --global)
      CONFIG_MODE="global"
      shift
      ;;
    --basic)
      CONFIG_DEPTH="basic"
      shift
      ;;
    --advanced)
      CONFIG_DEPTH="advanced"
      shift
      ;;
    --advanced-all)
      CONFIG_DEPTH="advanced-all"
      shift
      ;;
    *)
      echo "Usage: $0 [--project NAME] [--global] [--basic|--advanced|--advanced-all]" >&2
      exit 1
      ;;
  esac
done

if [[ -z "$TARGET_PROJECT" ]]; then
  echo "Project name cannot be empty" >&2
  exit 1
fi

BASE_ENV_FILE="$SCRIPT_DIR/.env"
PROJECT_ENV_DIR="$SCRIPT_DIR/projects"
PROJECT_ENV_FILE="$PROJECT_ENV_DIR/${TARGET_PROJECT}.env"
PROJECT_LOCAL_ENV_FILE="$PROJECT_ENV_DIR/${TARGET_PROJECT}.local.env"

prompt_value() {
  local __var_name="$1"
  local prompt_label="$2"
  local default_value="${3:-}"
  local response=""

  if [[ -n "$default_value" ]]; then
    read -r -p "$prompt_label [$default_value]: " response || true
    response="${response:-$default_value}"
  else
    read -r -p "$prompt_label: " response || true
  fi

  printf -v "$__var_name" '%s' "$response"
}

project_setting_default() {
  local key="$1"
  local fallback="${2:-}"
  local value=""

  if [[ -f "$PROJECT_ENV_FILE" ]]; then
    value="$(
      (
        set -a
        # shellcheck source=/dev/null
        . "$PROJECT_ENV_FILE" >/dev/null 2>&1 || true
        eval "printf '%s' \"\${$key-}\""
      )
    )"
  fi

  if [[ -n "$value" ]]; then
    printf '%s' "$value"
  else
    printf '%s' "$fallback"
  fi
}

write_setting() {
  local key="$1"
  local value="$2"
  printf '%s=%q\n' "$key" "$value" >> "$ENV_FILE"
}

default_remote="${RCLONE_REMOTE:-gdrive:}"

